public class PuntoRacionales
{
    //----------------------------------------------------------------------------------------------
    //---- Atributos -------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------    
 
    private Racional x;
    private Racional y;

    //----------------------------------------------------------------------------------------------
    //---- Constructor -----------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    
    public PuntoRacionales(Racional x, Racional y)
    {
        this.x = x;
        this.y = y;
    }

    //----------------------------------------------------------------------------------------------
    //---- Comandos --------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------

    public void establecerX (Racional punto)
    {
        x = punto;
    }

    public void establecerY (Racional punto)
    {
        y = punto;
    }
	 
    //----------------------------------------------------------------------------------------------
    //---- Consultas -------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    
    public Racional obtenerX()
    {
        return x;
    }
    
    public Racional obtenerY()
    {
        return y;
    }
}
