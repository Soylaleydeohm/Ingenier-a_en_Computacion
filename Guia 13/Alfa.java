
public class Alfa
{
    protected int n;
    protected String s;
    
    public Alfa(){
        n= 10;
        s= new String ("Alfa");
    }
    
    public String p(int x){
        return s+" p n: "+n+" x: "+x;
    }
    
    public String q(int x){
        return s+" q n: "+n+" x: "+x;
    }
    
    public String r(int x){
        return s+" r n: "+n+" x: "+x;
    }
    
    public String v(int x){
        return s+" v n: "+n+" x: "+x;
    }
}
